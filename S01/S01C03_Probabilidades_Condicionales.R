#Probabilidades condicionales

#Cuando los eventos no son independientes, las probabilidades condicionales son útiles. Ya vimos un ejemplo de probabilidad condicional: calculamos la probabilidad de que una segunda carta repartida sea un Rey dado que la primera fue un Rey. En probabilidad, usamos la siguiente notación:

#Pr(Carta 2 sea un rey∣ carta 1 sea un rey)= 3/51

#Usamos la ∣ como abreviatura de "dado que" o "condicional a".

#Cuando dos eventos, digamos A y B, son independientes, tenemos:

#Pr(A∣B)=Pr(A)

#Esta es la forma matemática de decir: el hecho de que suceda B no afecta la probabilidad de que suceda A. De hecho, esto puede considerarse la definición matemática de independencia.

